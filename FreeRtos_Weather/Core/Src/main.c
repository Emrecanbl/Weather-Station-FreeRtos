void Esp_8266_Task (void *argument)
{
	//192.168.1.6Wed:May:202412:56AM Format for Time transfer over Uart
	HAL_UART_Receive_DMA (&huart1, Rx_Byte, 31);
	while (1)
	{
		char *str = pvPortMalloc(50*sizeof(char));
		sprintf(str, "C=%d,H=%d,L=%d ",Temp,Hum,Light_level );  //Send Temperatute , Humanity , Light over Uart
		HAL_UART_Transmit (&huart1, str, 4*sizeof (str), 10);
		HAL_UART_Transmit (&huart1, "\r\n", sizeof ("\r\n"), 10);
		slice(Rx_Byte, ip, 0, 11);  //slicing Uart data
		slice(Rx_Byte, Date, 11, 23);
		slice(Rx_Byte, Time, 23, 28);
		slice(Rx_Byte, AmPm, 28, 31);
		for(int i=0 ; i<=32;i++){
				Rx_Byte[i] = 0;
			}
		vPortFree((void*)str);
		vTaskDelay(1000);
	}
}
void ADC_Task (void *argument)
{
	while (1)
	{
    // ADC Read with Ldr for Light Level
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 10);
		ADC_VAL = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);
		if (ADC_VAL>1700)
		{
			ADC_VAL = 1700;
			Light_level = 100;
		}
		else
		{
			Light_level = (uint8_t)((ADC_VAL/17));
		}

		vTaskDelay(1000);
	}
}

